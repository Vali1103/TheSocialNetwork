
class User:
    def __init__(self, username, name, password, cover_image, profile_image, about, age, location, gender, friends):
        self.username = username
        self.name = name
        self.password = password
        self.cover_image = cover_image
        self.profile_image = profile_image
        self.about = about
        self.age = age
        self.location = location
        self.gender = gender
        self.friends = friends




